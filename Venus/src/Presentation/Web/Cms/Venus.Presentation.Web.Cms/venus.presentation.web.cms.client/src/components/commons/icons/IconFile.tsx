const imageFormats = [".png", ".jpg", ".jpeg", ".gif", ".webp", ".svg", ".bmp"] as const;
type ImageFormat = typeof imageFormats[number];

const folderFormats = [".pdf"] as const;
type FolderFormat = typeof folderFormats[number];

export const IconTypeFile = (props :IconPropsBase & {type:ImageFormat|FolderFormat})=>{
  if (imageFormats.includes(props.type as ImageFormat)) {
    // ✅ props.type bir image format
    return <IConImage {...props} />;
  } else if (folderFormats.includes(props.type as FolderFormat)) {
    // ✅ props.type bir folder format
    return <IconPdf {...props} />;
  } else {
    return <IconFile2 {...props}></IconFile2>
  }
}